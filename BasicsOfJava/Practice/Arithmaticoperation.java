package BasicsOfJava.Practice;

public class Arithmaticoperation {
    public static void main(String[] args) {
        double x = 50, y = 10;
        System.out.println(x+y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x % y);
        System.out.println(x / y);
    }
}
